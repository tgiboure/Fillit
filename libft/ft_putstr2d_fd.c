#include "libft.h"

void	ft_putstr2d_fd(char **tab, int fd)
{
	int i;
	int k;

	i = 0;
	k = 0;
	write(fd, "{", 1);
	while (tab[k])
	{
		write(fd, "\"", 1);
		while (tab[k][i])
		{
			write(fd, &tab[k][i], 1);
			i++;
		}
		k++;
		i = 0;
		write(fd, "\", ", 3);
	}
	write(fd, "}", 1);
}
