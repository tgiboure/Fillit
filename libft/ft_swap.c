#include "libft.h"

void	ft_swap(void *a, void *b, size_t len)
{
	char *aa;
	char *bb;
	char c;

	aa = (char*)a;
	bb = (char*)b;
	while (len > 0)
	{
		c = *aa;
		*aa = *bb;
		*bb = c;
		aa++;
		bb++;
		len--;
	}
}
